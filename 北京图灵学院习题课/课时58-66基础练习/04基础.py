# #爱因斯坦曾出过一道有趣的数学题，有一个长梯阶，若每步上2阶，最后剩下2阶
# 若每步上5阶，最后剩4阶，若每步上6阶，最后剩下5阶，职业每步上7阶，最后刚好一阶不剩
x = 0
while x < 1000:
    if (x % 2 == 1) \
            and (x % 3 == 2) \
            and (x % 5 == 4) \
            and (x % 6 == 5) \
            and (x % 7 == 0):
        print(x)
        x += 1

    else:
        x += 1
print('循环结束')
